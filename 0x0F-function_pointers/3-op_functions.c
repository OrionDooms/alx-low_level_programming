#include "3-calc.h"
/**
 * op_add - sum program.
 * @a: integer passed as argument
 * @b: integer passed as argument
 * Return: result.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - difference program.
 * @a: integer passed as argument
 * @b: integer passed as argument
 * Return: result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - product program.
 * @a: integer passed as argument
 * @b: integer passed as argument
 * Return: result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division program.
 * @a: integer passed as argument
 * @b: integer passed as argument
 * Return: result.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder of the division program.
 * @a: integer passed as argument
 * @b: integer passed as argument
 * Return: result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
